GBitmap framebuffer_get_as_bitmap(FrameBuffer *fb, const GSize *size) {
    GBitmap bitmap = {0};

    bitmap.addr = fb->buffer;
    bitmap.bounds = GRect(0, 0, size->w, size->h);
    bitmap.row_size_bytes = FRAMEBUFFER_BYTES_PER_ROW;

    return bitmap;
}